struct font
{
  /* data */
  uint8_t width;
  uint8_t high;
  uint8_t index;
  uint8_t source;
  void *this;
} font_config = {
  8, 12, ASCII, BuildIn, ascii
};

static inline void font_init(uint8_t width, uint8_t high, uint8_t index, uint8_t source_type, void *font_offset)
{
  struct font tmp = { width, high, index, source_type, font_offset};
  font_config = tmp;
}

