void font_free()
{
  switch (font_config.index)
  {
    case UTF8:
    case Unicode:
        if (font_config.source == FileIn)
        {
            file_close(font_config.this);
        }
    case GBK:
    case GB2312:
    case ASCII:
    default:
      font_init(8, 12, ASCII, BuildIn, ascii);
      break;
  }
}

void font_load(uint8_t index, uint8_t width, uint8_t high, uint8_t source_type, void *src_addr)
{

    switch (index)
    {
    case UTF8:
        if (src_addr == NULL)
        {
            font_init(8, 12, ASCII, BuildIn, ascii);
            break;
        }
        font_init(width, high, UTF8, source_type, src_addr);
    break;
    default:
    case Unicode:
    case GBK:
    case GB2312:
    case ASCII:
        font_init(8, 12, ASCII, BuildIn, ascii);
    break;
    }
}

int font_get_utf8_size(const uint8_t pInput)
{
   uint8_t c = pInput;
   // 0xxxxxxx 1
   // 10xxxxxx -1
   // 110xxxxx 2
   // 1110xxxx 3
   // 11110xxx 4
   // 111110xx 5
    // 1111110x 6
    if(c< 0x80) return 1;
    if(c>=0x80 && c<0xC0) return -1;
    if(c>=0xC0 && c<0xE0) return 2;
    if(c>=0xE0 && c<0xF0) return 3;
    if(c>=0xF0 && c<0xF8) return 4;
    if(c>=0xF8 && c<0xFC) return 5;
    if(c>=0xFC) return 6;
    return 0;
}

int font_utf8_to_unicode(const uint8_t* pInput, uint64_t *Unic)
{  
    assert(pInput != NULL && Unic != NULL);

    char b1, b2, b3, b4, b5, b6;

    *Unic = 0x0;
    int utfbytes = font_get_utf8_size(*pInput);
    uint8_t *pOutput = (uint8_t *) Unic;

    switch ( utfbytes )
    {  
        case 1:  
            *pOutput     = *pInput;
            break;  
        case 2:  
            b1 = *pInput;
            b2 = *(pInput + 1);
            if ( (b2 & 0xE0) != 0x80 )
                return 0;  
            *pOutput     = (b1 << 6) + (b2 & 0x3F);
            *(pOutput+1) = (b1 >> 2) & 0x07;
            break;
        case 3:
            b1 = *pInput;
            b2 = *(pInput + 1);
            b3 = *(pInput + 2);
            if ( ((b2 & 0xC0) != 0x80) || ((b3 & 0xC0) != 0x80) )
                return 0;
            *pOutput     = (b2 << 6) + (b3 & 0x3F);
            *(pOutput+1) = (b1 << 4) + ((b2 >> 2) & 0x0F);
            break;
        case 4:
            b1 = *pInput;
            b2 = *(pInput + 1);
            b3 = *(pInput + 2);
            b4 = *(pInput + 3);
            if ( ((b2 & 0xC0) != 0x80) || ((b3 & 0xC0) != 0x80)
                    || ((b4 & 0xC0) != 0x80) )
                return 0;
            *pOutput     = (b3 << 6) + (b4 & 0x3F);
            *(pOutput+1) = (b2 << 4) + ((b3 >> 2) & 0x0F);
            *(pOutput+2) = ((b1 << 2) & 0x1C)  + ((b2 >> 4) & 0x03);
            break;
        case 5:
            b1 = *pInput;
            b2 = *(pInput + 1);
            b3 = *(pInput + 2);
            b4 = *(pInput + 3);
            b5 = *(pInput + 4);
            if ( ((b2 & 0xC0) != 0x80) || ((b3 & 0xC0) != 0x80)
                    || ((b4 & 0xC0) != 0x80) || ((b5 & 0xC0) != 0x80) )
                return 0;
            *pOutput     = (b4 << 6) + (b5 & 0x3F);
            *(pOutput+1) = (b3 << 4) + ((b4 >> 2) & 0x0F);
            *(pOutput+2) = (b2 << 2) + ((b3 >> 4) & 0x03);
            *(pOutput+3) = (b1 << 6);
            break;
        case 6:
            b1 = *pInput;
            b2 = *(pInput + 1);
            b3 = *(pInput + 2);
            b4 = *(pInput + 3);
            b5 = *(pInput + 4);
            b6 = *(pInput + 5);
            if ( ((b2 & 0xC0) != 0x80) || ((b3 & 0xC0) != 0x80)
                    || ((b4 & 0xC0) != 0x80) || ((b5 & 0xC0) != 0x80)
                    || ((b6 & 0xC0) != 0x80) )
                return 0;
            *pOutput     = (b5 << 6) + (b6 & 0x3F);
            *(pOutput+1) = (b5 << 4) + ((b6 >> 2) & 0x0F);
            *(pOutput+2) = (b3 << 2) + ((b4 >> 4) & 0x03);
            *(pOutput+3) = ((b1 << 6) & 0x40) + (b2 & 0x3F);
            break;
        default:
            return 0;
            break;
    }  

    return utfbytes;
}

